fun fullName (firstName:String,
              middleName:String,
              lastName:String){
    println("Hello $firstName, $middleName, $lastName")
}

fun namaSMP (first:String,
             second:String,
             third:String){
    println("Jaya $first $second $third")
}

fun main (){
    fullName(firstName = "Divaretta",
        middleName = "Kiesa",
        lastName = "Sumartha")
    namaSMP(first = "SMP",
        second = "3",
        third = "Cepu")
}